#Python Loops 2
#  For Loop 
programs ={"c++", "java","python"}
for x in programs:
    print (x)

#Looping Through a String 
for x in "java":
    print(x)
#The break Statement 
programs ={"c++", "java","python"}
for x in programs:
    print (x)
    if x =="java":
        break
#The continue Statement 
programs ={"c++", "java","python"}
for x in programs:
    print (x)
    if x =="java":
        continue
    print(x)
    


 
 